#Operadores Aritméticos com Tipos Diferentes

#Com Strings: A multiplicação de strings é uma forma de repetição:
texto = "Olá! "
resultado = texto * 3  # resultado é "Olá! Olá! Olá! "

#Com Listas: A multiplicação de listas também resulta na repetição:
lista = [1, 2, 3]
resultado = lista * 2  # resultado é [1, 2, 3, 1, 2, 3]