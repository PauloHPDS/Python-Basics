nome = "Paulo Henrique Pereia de Sousa"

#[start:stop:step]

#usar o indice para acessar o valor na string
print(nome[0])

#pega o valor na ordem invertida da string
print(nome[-2])

#[0:6] pega do inicio ate o 5
print(nome[:5])

#[6:0] pega de 6 ao final
print(nome[6:])

# pega entre 6 e 14
print(nome[6:14])

# Pega entre 6 e 14 de 2 em 2
print(nome[6:14:2])

#retorna uma copia da string
print(nome[:])

#cria uma copia da string invertida
print(nome[::-1])   