#Operadores de Atribuição
#Esses operadores combinam uma operação aritmética com uma atribuição.

#Adição e Atribuição (+=):
a = 10
a += 5  # equivalente a a = a + 5, resultado é 15

#Subtração e Atribuição (-=):
a = 10
a -= 3  # equivalente a a = a - 3, resultado é 7

#Multiplicação e Atribuição (*=):
a = 10
a *= 2  # equivalente a a = a * 2, resultado é 20

#Divisão e Atribuição (/=):
a = 10
a /= 2  # equivalente a a = a / 2, resultado é 5.0

#Divisão Inteira e Atribuição (//=):
a = 10
a //= 3  # equivalente a a = a // 3, resultado é 3

#Módulo e Atribuição (%=):
a = 10
a %= 3  # equivalente a a = a % 3, resultado é 1

#Exponenciação e Atribuição (**=):
a = 2
a **= 3  # equivalente a a = a ** 3, resultado é 8