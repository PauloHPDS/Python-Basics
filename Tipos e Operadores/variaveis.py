# Variaveis e Tipos de Dados

#Inteiros (int):
#Representam números inteiros.
idade = 25

#Ponto Flutuante (float):
#Representam números com casas decimais.
altura = 1.75

#Strings (str):
#Representam texto.
saudacao = "Olá, mundo!"

#Booleanos (bool):
#Representam valores lógicos, True ou False.
ligado = False

#Listas (list):
#Representam uma coleção ordenada de itens que podem ser de tipos diferentes.
frutas = ["maçã", "banana", "laranja"]

#Tuplas (tuple):
#Semelhantes às listas, mas imutáveis.
coordenadas = (10, 20)

#Dicionários (dict):
#Representam pares de chave-valor.
aluno = {"nome": "Pedro", "idade": 21}

#Conjuntos (set):
#Representam uma coleção de itens únicos e não ordenados.
cores = {"vermelho", "verde", "azul"}